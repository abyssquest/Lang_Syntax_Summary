// 문자열 자료형
console.log('Hello, JavaScript'); // 작은 따옴표
console.log("Hello, JavaScript"); // 큰 따옴표

// 문자열안에 따옴표를 쓰고 싶다면
// 1. 감싸는 따옴표와 다른 따옴표는 안에 쓸 수 있다
console.log('Hello, "JavaScript"');
console.log("Hello, 'JavaScript'");
// 2. 이스케이프 문자를 쓴다
console.log('Hello, \'JavaScript\''); // 작은 따옴표
console.log("Hello, \"JavaScript\""); // 큰 따옴표
