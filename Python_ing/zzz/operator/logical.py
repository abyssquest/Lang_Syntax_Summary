# 논리 연산자
t = True; f = False;

# and 연산자 - && 쓰지 않는다
# 논리형 데이터 2개 입력
# 논리형 데이터 1개 출력
print("and 연산자 연습")
print(t and t)
print(t and f)
print(f and t)
print(f and f)

# or 연산자 - || 쓰지 않는다
# 논리형 데이터 2개 입력
# 논리형 데이터 1개 출력
print("or 연산자 연습")
print(t or t)
print(t or f)
print(f or t)
print(f or f)

# not 연산자
# 논리형 데이터 1개 입력
# 논리형 데이터 1개 출력
print("not 연산자 연습")
print(not t)
print(not f)