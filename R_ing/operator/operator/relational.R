# 비교(관계) 연산자
num1 <- 100 # 피연산자1
num2 <- 20  # 피연산자2

# (1) 동등비교
boolean <- num1 == num2 # 두 변수의 값이 같은지 비교 
boolean
boolean <- num1 != num2 # 두 변수의 값이 다른지 비교 
boolean

# (2) 크기비교
boolean <- num1 > num2 
boolean
boolean <- num1 >= num2 
boolean
boolean <- num1 < num2 
boolean
boolean <- num1 <= num2 
boolean